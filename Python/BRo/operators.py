#ADDITION OPERATORS

friends = 15

friends = friends + 1
friends += 1
print(friends)


#SUBSTRACTION OPERATORS
friends = friends - 1
friends -= 1

print(friends)

#MULTIPLICATION OPERATORS

friends = friends * 3
friends *= 3
print(friends)

#DIVISION OPERATORS

friends = friends / 2
friends /= 1
print(friends)

#EXPOTENT OPERATORS
#" ** represents TO the power"
friends = friends ** 2
friends **= 2

print(friends)

