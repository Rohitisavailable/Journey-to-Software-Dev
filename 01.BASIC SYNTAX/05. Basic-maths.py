a = 2
# a is a variable that stores the integer 2

b = 4
# b is a variable that stores the integer 4

# a and b are variables that store integer values
############################################################################
#1. for addition
c = a + b
# c is a variable that stores the result of adding a and b
print(c)
############################################################################
#2. for subtraction

d = a - b
# d is a variable that stores the result of subtracting b from a
print(d)
############################################################################
#3. for multiplication

e = a * b
# e is a variable that stores the result of multiplying a and b
# * is the multiplication operator
print(e)
############################################################################
#4. for division

f = a / b
# f is a variable that stores the result of dividing a by b
# / is the division operator
print(f)
