g = a + b - a * b / a
# g is a variable that stores the result of the expression a + b - a * b / a
# This expression combines addition, subtraction, multiplication, and division
# The order of operations is followed: multiplication and division are performed before addition and subtraction
# The result is calculated as follows:
# 1. a * b = 2 * 4 = 8
# 2. a + b = 2 + 4 = 6
# 3. 8 / a = 8 / 2 = 4  
# 4. 6 - 4 = 2
# g is a variable that stores the final result of the expression

print(g)

